#include <iostream>

using namespace std;


main(){

    int  a = 10 , b = 5;

    // equals to
    cout << (a == b) << endl;

    //not equals to
    cout << (a != b) << endl;

    //greater than
    cout << (a > b) << endl;

    //less than
    cout << (a < b) << endl;

    //greater than or equals to
    cout << (a >= b) << endl;

     //less than or equals to
     cout << (a <= b) << endl;

}
